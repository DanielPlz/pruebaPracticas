                <h4>Descripción del Servicio</h4>
                <br>
                <span>ConexiónSalud, a través de su sitio “www.psicologostemuco.cl”, pone a disposición a sus pacientes,
                una plataforma web para consultas clínicas, en modalidad online como también en modalidad
                presencial con un psicólogo del área de la salud acreditado por cualquier casa de estudios
                reconocida por el estado de Chile. Debe tener experiencia acreditada en el área de su expertise. Los
                psicólogicos deben estar calificados y certificados por su respectivo consejo psicólogico después de
                completar con éxito la educación, exámenes y capacitaciones necesarias de la práctica
                correspondiente. <br>
                Que esté inscrito en el Registro Nacional de Prestadores Individuales de Salud (RNPI) de la
                Superintendencia de Salud de Chile
                Esta plataforma ha exigido el cumplimiento de todas las disposiciones legales para proteger y
                resguardar la información personal de sus pacientes. Esta plataforma permite el agendamiento
                online para realizar su atención médica y/o clínica realizada en diferentes modalidades. Más detalles
                se especificarán.<br>
                Los psicólogos del área de la salud son proveedores independientes que no son nuestros
                empleados ni agentes ni representantes. El rol de la plataforma se limita a habilitar citas de los
                servicios de salud, información, entre otras, que usted puede necesitar, mientras que los servicios de
                salud son responsabilidad de los psicólogos mismos que lo proporcionan directa o indirectamente.
                Si considera que los servicios de salud proporcionados por el psicólogo no se ajustan a sus
                necesidades o expectativas, puede cambiar a un psicólogo de área de la salud diferente que brinde
                servicios a través de la plataforma.<br>
                Todos los psicólogos u otros profesionales de la salud (kinesiólogos, fonoaudiólogos, médicos,
                enfermeros y otros que en un futuro se agregaran) ofrecen sus servicios libremente en la ejecución
                de su profesión. Cualquier información, recomendación, indicación, diagnóstico, prescripción o
                tratamiento emanado o recibido a través de la consulta de ConexiónSalud proviene y es dada
                exclusivamente a dichos psicólogos de acuerdo con información que usted provee en una relación
                directa psicólogo-paciente.<br>
                </span>
                <br>
                <h4>La Consulta de ConexiónSalud incluye las siguientes prestaciones.</h4>
                <br>
                <span>● Consulta médica y otras profesiones: La consulta de ConexiónSalud es entregada por un
                psicólogo u otro profesional del área de la salud, enfocado en atención ambulatoria y
                orientación de la salud. En esta modalidad, el psicólogo atiende al paciente mediante las
                TICs (técnicas de la información y comunicación) como por ejemplo video llamadas, telefonía,
                entre otras, consiguiendo a través de esta plataforma, los antecedentes clínicos del paciente
                que sean necesarios para dar curso a la atención, diagnosticar y entregar un tratamiento.
                <br>
                ● Receta médica: En aquellos casos que correspondiera, conforme al criterio médico del
                psicólogo que realizará la consulta, al finalizar la atención el paciente recibirá una receta
                medica simple para adquirir los medicamentos del tratamiento indicado por el médico. En el
                caso de corresponder a una receta medica retenida, el psicólogo podrá digitalizarla y
                enviarla a su paciente.<br>
                ● Certificados médicos: El médico podrá entregar al paciente certificados de atención médica,
                válido como justificativo para ser presentado en la Institución correspondiente como también
                podrá entregar certificados de derivaciones médicas.<br>
                ● Órdenes de examen: El médico podrá emitir órdenes de exámenes y/o procedimientos, que
                permitan verificar, identificar o descartar un determinado diagnóstico.<br>
                </span>
                <br>
                <h4>Exclusiones</h4>
                <br>
                <span>⮚ Los servicios de PsicólogosTemuco no incluye las prestaciones asociadas a laboratorios clínicos.<br>
                ⮚ Los servicios de ConexiónSalud excluye atenciones de urgencia o emergencia médica. De ser
                así debe acudir inmediatamente a un establecimiento de salud que pueda otorgar esta
                prestación.<br>
                ⮚ Los servicios de PsicólogosTemuco excluye la emisión y/o prescripción de recetas retenidas y
                recetas cheque.<br>
                </span>
